int twoStacks(int maxSum, vector<int> a, vector<int> b) {

    int sum = 0;
    int terms = 0;
    a.emplace_back(numeric_limits<int>::max());
    b.emplace_back(numeric_limits<int>::max());
    auto aptr = a.begin();
    auto bptr = b.begin();
    
    while (sum <= maxSum) {
        if (*aptr < *bptr) {
            sum += *aptr;
            aptr++;            
        }
        else {
            sum += *bptr;
            bptr++;            
        }
        terms++;
    }
    return terms-1;
}

